msg='Hello'
#Positive Indexing
a=msg[0]
print(a) #Result H

b=msg[1] 
print(b) #Result e

c=msg[2]
print(c) #Result l

d=msg[3]
print(d) #Result l

e=msg[4]
print(e) #Result o

#Negative Indexing
f=msg[-1]
print(f) #o

h=msg[-2]
print(h)  #l

i=msg[-3]
print(i) #l

j=msg[-4]
print(j) #e

k=msg[-5]
print(k) #H
